import {useCurrentFilmSelector} from '../../../store/film/selectors.ts';
import {formatDuration} from '../../../helpers/formatter';

export function DetailsTab(): JSX.Element {
  const data = useCurrentFilmSelector().data;
  return (
    <div className="film-card__text film-card__row">
      <div className="film-card__text-col">
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Director</strong>
          <span className="film-card__details-value">{data?.director}</span>
        </p>
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Starring</strong>
          <span className="film-card__details-value">
            {data?.starring.map((actor) => (
              <>
                {actor} <br />
              </>
            ))}
          </span>
        </p>
      </div>

      <div className="film-card__text-col">
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Run Time</strong>
          <span className="film-card__details-value">
            {formatDuration(data?.runTime || 0)}
          </span>
        </p>
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Genre</strong>
          <span className="film-card__details-value">{data?.genre}</span>
        </p>
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Released</strong>
          <span className="film-card__details-value">{data?.released}</span>
        </p>
      </div>
    </div>
  );
}

